========================================================

[필수 요구사항]

1. 카드

데이터
- 카드 숫자를 저장한다.
- 카드의 현재 상태(앞면, 뒷면)를 저장한다.
- 카드 앞면 이미지를 저장한다.
- 카드 뒷면 이미지를 저장한다.

기능
- 카드 생성 시 기본 상태는 뒷면이다.
- 카드 상태를 앞면으로 변경할 수 있다.
- 카드 상태를 뒷면으로 변경할 수 있다.
- 상태 변경 시 이미지도 함께 변경된다.
- 현재 상태를 반전할 수 있다.


--------------------------------------------------------

2. 카드 선택

데이터
- 여러 장의 카드를 관리한다.
- 현재 선택 중인 카드 위치를 저장한다.
- 커서를 관리한다.

기능
- 왼쪽 입력 시 이전 카드 선택
- 오른쪽 입력 시 다음 카드 선택
- 선택 위치 변경 시 커서 이동
- 첫 번째 카드에서 왼쪽 이동 시 마지막 카드 선택
- 마지막 카드에서 오른쪽 이동 시 첫 번째 카드 선택


--------------------------------------------------------

3. 카드 선택 동작

기능
- 현재 선택된 카드를 선택할 수 있다.
- 선택된 카드의 상태를 변경할 수 있다.


--------------------------------------------------------

4. 카드 제거

기능
- 카드 두 장을 전달받는다.
- 카드 두 장을 제거할 수 있다.
- 카드 제거 시 카드가 포함된 GameObject도 제거한다.


