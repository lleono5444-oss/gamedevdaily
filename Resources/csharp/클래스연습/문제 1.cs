    -만약 산 이력이 있다면
        -아이템을 상점이 사준다
            -해당 아이템의 스탯에 대한 빼기를 진행한다
            -판매아이템의 값에 해당하는 골드를 보유골드에 더해준다.
        -
4. 클래스 간 상호작용

5. 설계 이유
1.아이템의 주인은 상점이다
