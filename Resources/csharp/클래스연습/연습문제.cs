    0. Player.isDead= false 일때만 활성화된다.
    1.회복선택후 오브젝트 선택시 해당하는 회복량이나 버프를 제공한다.
        풀피일대는 회복류(소모품)을 소모하지 않고 넘어가며, 포션 개수는 그대로를 유지한다.

