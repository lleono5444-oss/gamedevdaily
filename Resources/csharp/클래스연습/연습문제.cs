/* 연습 문제  - 체력 포션 사용 시스템


조건
1. 플레이어는 현재 체력(CurrentHp)과 최대 체력(MaxHp)을 가진다.
2. 포션은 회복량(HealAmount)을 가진다.
3. 포션을 사용하면 플레이어의 체력이 회복된다.
4. 체력은 최대 체력을 초과할 수 없다.

예시

플레이어
현재 체력 : 40
최대 체력 : 100

포션
회복량 : 30

결과
현재 체력 : 70

