//Player

//데이터
//├ CurrentHp
//├ MaxHp
//├ IsDead

//책임
//├ 자신의 체력을 관리한다.
//├ 회복 요청을 받으면 체력을 증가시킨다.
//├ 체력이 MaxHp를 초과하지 않도록 한다.
//├ 체력이 0 이하가 되면 IsDead를 true로 변경한다.
//└ 죽은 상태에서는 회복을 거부한다.

//--------------------------------------------------

//Potion

//데이터
//├ HealAmount
//├ Count

//책임
//├ 회복량 정보를 제공한다.
//├ 사용 시 HealAmount를 Player에게 전달한다.
//├ 사용 가능 여부를 확인한다.
//└ 사용 성공 시 개수를 차감한다.

//--------------------------------------------------

//PlayerController

//데이터
//없음

//책임
//├ 플레이어 입력을 처리한다.
//├ 포션 사용 입력을 감지한다.
//├ 스킬 사용 입력을 감지한다.
//└ 필요한 객체(Player, Potion)에 요청을 전달한다.

//--------------------------------------------------

//클래스 간 상호작용

//1. 플레이어가 포션 사용 버튼 입력
//2. PlayerController가 입력 감지
//3. PlayerController가 Potion 사용 요청
//4. Potion이 HealAmount 제공
//5. Player가 Heal(HealAmount) 실행
//6. Player가 체력 검증
//   - 최대 체력 초과 방지
//   - 사망 상태 확인
//7. 회복 완료

//--------------------------------------------------

//설계 이유

//1. 체력 데이터의 주인은 Player이다.
//2. 체력 변경 규칙도 Player가 관리한다.
//3. Potion은 회복량만 제공한다.
//4. PlayerController는 입력만 담당한다.
