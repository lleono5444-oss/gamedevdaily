아래 형식으로 작성

Player
 ├ 데이터 : 플레이어의 체력,플레이어의 최대체력, 힐 스킬(일정량 회복시킨다), 회복 버프 스킬(일정 시간동안 시간에따라 일정량을 같은 량만큼 회복시킨다)
Potion
 ├ 데이터 : 회복량(healAmount),포션 개수,음식(포션의 한 종류로써 다룸)

PlayerController: 
데이터 플레이어 상태(살음/죽음),
책임 플레이어 체력을 회복한다, 플레이어 스킬 구현
      예외처리
            1. 
            2. 
            3. 
            4. 체력이 0이하 일땐 포션을 사용할수 없다.

--------------------------------------------------

1차 피드백후 

Player 
데이터플레이어 현재체력,최대체력, 스킬리스트(힐 스킬,회복 버프), isDead
책임 
    0. 스킬에 관하여 isDead= false 일때만 활성화된다.
    1. 플레이어의 체력을 확인하고 그에 따라 isDead(true/false)
        1.1 체력이 0이하가 되었을때는 체력을 0으로 재할당하며 isDead = true;
    
    2.힐 스킬 실행시 즉시 체력을 회복한다
        2.1.현재 체력이 회복될 떄 최대체력을 넘어갈시 최대체력으로 재할당한다
        2.2 풀피일때는 회복 스킬 자체가 선택이 안되며 현재체력이 최대체력보다 낮을때만 사용가능하다 또한 살아있어야한다.
    3.힐 버프 실행시 일정 시간동안 간격을 두고 힐을 제공한다
        예외는 2.1과2.2를 동일하게 따른다
        

회복(소모품)
데이터 음식,포션
